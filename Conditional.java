public class Conditional {
    public static void main(String[]args)
    {
        int x= 56;
        int f= 54;
        int G= 44;

        
         if(x>f && f>G)
         {
            System.out.println(x);
         }
         else
             System.out.println(G);    

        if(G>f)
        {
            System.out.println(f);
        } 
        else
            System.out.println(x);    
          
             
    }
    
}
